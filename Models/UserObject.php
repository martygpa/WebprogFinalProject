class UserObject
{
  public $firstName;
  public $lastName;
  public $userName;
  public $password;

  function UserObject($firstName, $lastName, $userName, $password)
  {
    $this->firstName = $firstName;
    $this->lastName = $lastName;
    $this->userName = $userName;
    $this->password = $password;
  }
}


































